#!/usr/bin/env node
/**
 * Snapshot the live Supabase Showcase order into a static, synchronously
 * loadable file: data/showcase-order.js (defines window.TW_SHOWCASE_ORDER).
 *
 * Why: hybrid-local mode paints the collection grid from data/wardrobe.js
 * (a synchronous <script>, no network) before the deferred Supabase fetch
 * lands. Without the showcase order at that moment, the first paint shows
 * the whole catalogue, then snaps to the curated pieces ~1s later — a
 * visible jump on first visit. Loading this file (also a synchronous
 * <script>, before app.js) gives the very first paint the correct order on
 * any device with zero network. The live fetch + localStorage cache still
 * provide freshness on top.
 *
 * Re-run whenever you reorder the Showcase in the Account page:
 *   npm run db:bake-showcase
 *
 * Source of truth: wardrobe_items.showcase_order (falls back to
 * metadata.showcase_rank). Output is dense-ranked 0..N by ascending order.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createClient } from "@supabase/supabase-js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function loadEnvFile() {
  const envPath = path.join(root, ".env");
  if (!fs.existsSync(envPath)) return;
  for (const line of fs.readFileSync(envPath, "utf8").split("\n")) {
    const m = line.match(/^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/);
    if (!m) continue;
    let val = m[2].trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    if (!process.env[m[1]]) process.env[m[1]] = val;
  }
}

loadEnvFile();

const url = process.env.SUPABASE_URL;
const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!url || !serviceKey) {
  console.error("Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in .env");
  process.exit(1);
}

const client = createClient(url, serviceKey, {
  auth: { persistSession: false, autoRefreshToken: false },
});

const { data, error } = await client.from("wardrobe_items").select("id, showcase_order, metadata");
if (error) {
  console.error("Fetch failed:", error.message);
  process.exit(1);
}

// {id → raw order value} from showcase_order, falling back to metadata.showcase_rank.
const rawOrderById = new Map();
for (const row of data || []) {
  const id = String(row?.id ?? "").trim();
  if (!id) continue;
  let order = typeof row?.showcase_order === "number" ? row.showcase_order : null;
  if (order == null) {
    const meta = row?.metadata && typeof row.metadata === "object" ? row.metadata : null;
    if (typeof meta?.showcase_rank === "number") order = meta.showcase_rank;
  }
  if (typeof order === "number" && order >= 0) rawOrderById.set(id, order);
}

// Dense-rank 0,1,2,… by ascending raw order; emit id-sorted for a stable diff.
const ranked = [...rawOrderById.entries()].sort((a, b) => a[1] - b[1]);
const denseById = new Map();
ranked.forEach(([id], i) => denseById.set(id, i));
const orderObj = {};
for (const id of [...denseById.keys()].sort()) orderObj[id] = denseById.get(id);

const outPath = path.join(root, "data", "showcase-order.js");
const banner =
  "/* Generated by `npm run db:bake-showcase` — do not edit by hand.\n" +
  ` * Snapshot of the Supabase Showcase order (${ranked.length} pieces) at ${new Date().toISOString()}.\n` +
  " * Loaded as a synchronous <script> before app.js so the first collection paint is flash-free. */\n";
fs.writeFileSync(outPath, `${banner}window.TW_SHOWCASE_ORDER = ${JSON.stringify(orderObj, null, 2)};\n`, "utf8");

console.log(`Showcase order snapshot → ${path.relative(root, outPath)} (${ranked.length} pieces).`);
console.log("Commit data/showcase-order.js to ship a flash-free first paint.");
